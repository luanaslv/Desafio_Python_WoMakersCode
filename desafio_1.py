#função operações
def operations(a,b):
  print("Adição (a + b) = ", a+b)
  print("Subtração (a - b) = ", a-b)
  print("Multiplicação (a x b) = ", a*b)
  print("Divisão (a / b) = %.1f" % (a/b))

#teste 1
